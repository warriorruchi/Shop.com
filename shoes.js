function displayfun(q) {
    document.querySelector("#product-container").innerHTML = "";
    q.map(function (elem, i) {
        var box = document.createElement("div");

        var im = document.createElement("img");
        im.setAttribute("src", elem.img);

        var na = document.createElement("h3");
        na.innerText = elem.name;

        var sbp = document.createElement("p");

        var logo = document.createElement("img");
        logo.setAttribute("src", "https://img.shop.com/Image/resources/images/onecart-icon.svg");
        // logo.setAttribute("class", "cartlogo");
        logo.style.height = "15px"
        logo.style.width = "15px";

        var sb = document.createElement("span");
        sb.innerText = elem.soldBy;
        sb.setAttribute("class", "sold");
        sbp.append(logo, sb);
        sbp.style.marginTop = "-15px";

        var pr = document.createElement("p");

        var sp1 = document.createElement("span");
        sp1.innerText = elem.Strikedprice;
        sp1.style.fontSize = ".75rem";
        sp1.style.textDecoration = "line-through";
        var sp2 = document.createElement("span");
        sp2.innerText = elem.price;
        sp2.setAttribute("class", "oprice");
        pr.append(sp1, sp2);
        pr.style.marginTop = "-6px";

        var cbp = document.createElement("p");

        var rato = document.createElement("span");
        rato.innerText = elem.ratio;
        rato.style.marginRight = "5px";
        var logo2 = document.createElement("img");
        logo2.setAttribute("src", "https://img.shop.com/Image/resources/images/cashback-icon.svg");
       //logo2.setAttribute("class", "dollar");
        logo2.style.height = "13px";
        logo2.style.width = "13px";
        var cb = document.createElement("span");
        cb.innerText = "Cashback";
        cbp.append(rato, logo2, cb);
        cbp.style.fontSize = ".75rem";
        cbp.style.marginTop = "-8px";


        var sp = document.createElement("span");
        sp.innerText = elem.shipping;
        sp.style.color = "#007e97";
        sp.style.fontSize = ".75rem";
        sp.style.marginBottom = "15px";

        var bdiv = document.createElement("div");
        bdiv.setAttribute("class", "buttons")

        var sd = document.createElement("div");
        
        var anchor=document.createElement("a");
        anchor.innerText="See Details";
        sd.append(anchor);
        anchor.setAttribute("href","details.html");
        anchor.setAttribute("class","anch");
        anchor.addEventListener("click",function(){
            det(elem);
        });
        //sd.setAttribute("class", "details");
        var atc = document.createElement("button");
        atc.innerText = "Add to Cart";
        atc.addEventListener("click",function(){
            cart(elem,i);
        });

       // atc.setAttribute("class", "addtocart");
        bdiv.append(sd, atc)

        box.append(im, na, sbp, pr, cbp, sp, bdiv);
        document.getElementById("product-container").append(box);


    });
}

